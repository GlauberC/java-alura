
public class TestaCondicional {
	public static void main(String[] args) {
		int idade = 64;

		if (idade < 18) {
			System.out.println("Menor de 18");
		} else if (idade >= 65) {
			System.out.println("Você tem mais que 65 anos");
		} else {
			System.out.println("Você tem mais de 18 anos");
		}
	}
}
